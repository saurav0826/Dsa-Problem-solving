package BitwiseOperator;

public class BitBasic_2 {
	public static void main (String[] args) {
	    
	    int o = -1;
	    
	    System.out.println(o << 1);

	    
	    int x = 3;
	    
	    System.out.println(x << 1);
	    
	    System.out.println(x << 2);
	    
	    int y = 4;
	    
	    int z = (x << y);
	    
	    System.out.println(z);
	    
	    int k = 1;
	    
		System.out.println(~ k);
	    
	}
}
